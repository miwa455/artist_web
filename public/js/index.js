$(".bi-star").click(function () {
    $(this).toggleClass('active');
    // SVGを別のSVGに変更する
    if ($(this).hasClass('active')) {
        // bi-star.active クラスが追加された場合は、別のSVGに変更する
        $(this).find('img').attr('src', '../images/');
    } else {
        // bi-star.active クラスが削除された場合は、元のSVGに戻す
        $(this).find('img').attr('src', 'path/to/original-svg.svg');
    }
});


